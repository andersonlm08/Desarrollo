    private boolean validarDatosObligatorios() {
        if (StringUtils.isEmpty(txtNit.getText())) {
            JOptionPane.showMessageDialog(null, "Debe digitar una identificación", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (StringUtils.isEmpty(txtProveedor.getText())) {
            JOptionPane.showMessageDialog(null, "Debe digitar una descripción del proveedor", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

    private void cargarDatosAcampos() {
        int[] rows = jTable1.getSelectedRows();
        for (int i = rows.length - 1; i >= 0; i--) {
            ProveedorDTO proveedor = adapter.consultarProveedor((String) modelo.getValueAt(jTable1.getSelectedRow(), 0));
            txtNit.setEditable(false);
            txtNit.setText(proveedor.getNitDocumento());
            txtProveedor.setText(proveedor.getNombreComercial());
            txtDireccion.setText(proveedor.getDireccion());
            txtTelefono.setText(proveedor.getTelefono());
            txtFax.setText(proveedor.getFax());
            txtEmail.setText(proveedor.getEmail());
            txtWeb.setText(proveedor.getWeb());
            txtRepresentante.setText(proveedor.getRepresentante());
        }
    }

