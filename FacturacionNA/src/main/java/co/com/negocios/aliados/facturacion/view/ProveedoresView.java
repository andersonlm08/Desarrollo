    private void eliminarProveedor() {
        int[] rows = jTable1.getSelectedRows();
        for (int i = rows.length - 1; i >= 0; i--) {
            adapter.eliminarProveedor((String) modelo.getValueAt(jTable1.getSelectedRow(), 0));
            refrescarTabla();
        }
    }

    private ProveedorDTO getProveedor() {
        ProveedorDTO proveedorDTO = new ProveedorDTO();
        proveedorDTO.setDireccion(txtDireccion.getText());
        proveedorDTO.setEmail(txtEmail.getText());
        proveedorDTO.setFax(txtFax.getText());
        proveedorDTO.setNitDocumento(txtNit.getText());
        proveedorDTO.setNombreComercial(txtNit.getText());
        proveedorDTO.setRepresentante(txtRepresentante.getText());
        proveedorDTO.setTelefono(txtTelefono.getText());
        proveedorDTO.setWeb(txtWeb.getText());
        CiudadDTO ciudadDTO = new CiudadDTO();
        ciudadDTO.setCodigoCiudad("01");
        proveedorDTO.setCodCiudadDTO(ciudadDTO);        
        return proveedorDTO;
    }
