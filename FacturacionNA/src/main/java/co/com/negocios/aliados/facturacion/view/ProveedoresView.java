    public void refrescarTabla() {
        modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        modelo.addColumn("Nit ");
        modelo.addColumn("Proveedor");
        modelo.addColumn("Dirección");
        modelo.addColumn("Teléfono");
        modelo.addColumn("Fax");
        modelo.addColumn("Email");

        List<ProveedorDTO> listaProveedor = adapter.consultarProveedores();

        listaProveedor.forEach((proveedorDTO) -> {
            modelo.addRow(new Object[]{proveedorDTO.getNitDocumento(), proveedorDTO.getNombreComercial(),
                proveedorDTO.getDireccion(), proveedorDTO.getTelefono(), proveedorDTO.getFax(), proveedorDTO.getEmail()});
        });
        jTable1.setModel(modelo);
    }


