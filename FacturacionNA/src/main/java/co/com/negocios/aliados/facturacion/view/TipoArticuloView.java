    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        LOGGER.info("Inicio elimiar tipo articulo");
        try {
            eliminarCliente();
        } catch (Exception ex) {
            LOGGER.error(ExceptionUtils.getFullStackTrace(ex));
            JOptionPane.showMessageDialog(null, "Ocurrió un error inesperado, comuniquese con el adminstrador del sistema", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            
        }
        LOGGER.info("Fin eliminar tipo articulo");
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        LOGGER.info("Inicio guardar tipo articulo");
        try {
            if (validarDatosObligatorios()) {
                TipoArticuloDTO tipoArticuloDTO = new TipoArticuloDTO();
                tipoArticuloDTO.setDescripcion(txtDescripcion.getText());
                bean.guardarTipoArticulo(tipoArticuloDTO);
                refrescarTabla();
                limpiarCampos();
                JOptionPane.showMessageDialog(null, "Registro exitoso", "MENSAJE DEL SISTEMA", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Ya se encuentra registrado el producto con esa referenca", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception ex) {
            LOGGER.error(ExceptionUtils.getFullStackTrace(ex));
            JOptionPane.showMessageDialog(null, "Ocurrió un error inesperado, comuniquese con el adminstrador del sistema", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
        }
        LOGGER.info("Fin guardar tipo articulo");
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        LOGGER.info("Inicio actualizar tipo articulo");
        try {
            TipoArticuloDTO tipoArticuloDTO = new TipoArticuloDTO();
            tipoArticuloDTO.setCodigoTipoArticulo(new Long(txtCodigo.getText()));
            tipoArticuloDTO.setDescripcion(txtDescripcion.getText());
            bean.actualizarTipoArticulo(tipoArticuloDTO);
            refrescarTabla();
            limpiarCampos();
            JOptionPane.showMessageDialog(null, "Registro exitoso", "MENSAJE DEL SISTEMA", JOptionPane.INFORMATION_MESSAGE);
        } catch (NumberFormatException | HeadlessException ex) {
            LOGGER.error(ExceptionUtils.getFullStackTrace(ex));
            JOptionPane.showMessageDialog(null, "Ocurrió un error inesperado, comuniquese con el adminstrador del sistema", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
        }
        LOGGER.info("Fin actualizar tipo articulo");
    }//GEN-LAST:event_btnActualizarActionPerformed

