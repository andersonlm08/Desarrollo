    public void refrescarTabla() {
        modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        modelo.addColumn("Consecutivo");        
        modelo.addColumn("Descripción");        
        
        List<TipoArticuloDTO> listaTipoArticulo = bean.consultarTipoArticulos();
        
        listaTipoArticulo.forEach((tipoArticuloDTO) -> {
            modelo.addRow(new Object[]{tipoArticuloDTO.getCodigoTipoArticulo(), tipoArticuloDTO.getDescripcion()});
        });
        
        jTable1.setModel(modelo);
    }
    
    private void eliminarCliente() {
        int[] rows = jTable1.getSelectedRows();
        for (int i = rows.length - 1; i >= 0; i--) {
            bean.eliminarTipoArticulo((Long) modelo.getValueAt(jTable1.getSelectedRow(), 0));
            refrescarTabla();
        }
    }
    
    private void limpiarCampos() {
        txtCodigo.setText("");
        txtDescripcion.setText("");
    }
    
    private void cargarDatosAcampos() {
        int[] rows = jTable1.getSelectedRows();
        for (int i = rows.length - 1; i >= 0; i--) {            
            TipoArticuloDTO tipoArticuloDto = bean.consultarTipoArticulo((Long) modelo.getValueAt(jTable1.getSelectedRow(), 0));
            txtDescripcion.setText(tipoArticuloDto.getDescripcion());
            txtCodigo.setText(tipoArticuloDto.getCodigoTipoArticulo() + "");
        }
    }

