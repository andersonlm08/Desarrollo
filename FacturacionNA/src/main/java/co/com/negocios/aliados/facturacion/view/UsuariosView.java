    public void refrescarTabla() {
        modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        modelo.addColumn("Documento");
        modelo.addColumn("Nombres");
        modelo.addColumn("Apellidos");
        modelo.addColumn("Email");
        modelo.addColumn("Usuario");
        modelo.addColumn("Teléfono");

        UsuariosBean bean = new UsuariosBean();
        List<Usuario> listaUsuarios = bean.consultarUsuarios();

        for (Usuario usuario : listaUsuarios) {
            modelo.addRow(new Object[]{usuario.getDocumento(), usuario.getNombres(), usuario.getApellidos(),
                usuario.getEmail(), usuario.getUsuario(), usuario.getTelefono()});

        }
        jTable1.setModel(modelo);
    }

    private void eliminarCliente() {
        int[] rows = jTable1.getSelectedRows();
        for (int i = rows.length - 1; i >= 0; i--) {
            bean.eliminarUsuario((String) modelo.getValueAt(jTable1.getSelectedRow(), 0));
            refrescarTabla();
        }
    }

    private void limpiarCampos() {
        txtDocumento.setEditable(true);
        txtClave.setText("");
        txtConfirmarClave.setText("");
        txtUsuario.setText("");
        txtNombres.setText("");
        txtApellidos.setText("");
        txtTelefono.setText("");
        txtEmail.setText("");
    }

    private void cargarDatosAcampos() {
        int[] rows = jTable1.getSelectedRows();
        for (int i = rows.length - 1; i >= 0; i--) {
            Usuario usuario = bean.consultarUsuario((String) modelo.getValueAt(jTable1.getSelectedRow(), 4));
            txtDocumento.setEditable(false);
            txtUsuario.setText(usuario.getUsuario());
            txtDocumento.setText(usuario.getDocumento());
            txtNombres.setText(usuario.getNombres());
            txtApellidos.setText(usuario.getApellidos());
            txtTelefono.setText(usuario.getTelefono());
            txtEmail.setText(usuario.getEmail());
        }
    }

