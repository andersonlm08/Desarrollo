    private boolean validarDatosObligatorios() {
        if (StringUtils.isEmpty(txtUsuario.getText())) {
            JOptionPane.showMessageDialog(null, "Debe digitar un usuario", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!validarContrasenia(new String(txtClave.getPassword()), new String(txtConfirmarClave.getPassword()))) {
            JOptionPane.showMessageDialog(null, "Contraseñas no válidas", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (StringUtils.isEmpty(txtDocumento.getText())) {
            JOptionPane.showMessageDialog(null, "Debe digitar un documento", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (StringUtils.isEmpty(txtNombres.getText())) {
            JOptionPane.showMessageDialog(null, "Debe digitar un nombre", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (StringUtils.isEmpty(txtApellidos.getText())) {
            JOptionPane.showMessageDialog(null, "Debe digitar un apellido", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (StringUtils.isEmpty(txtTelefono.getText())) {
            JOptionPane.showMessageDialog(null, "Debe digitar un teléfono", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (StringUtils.isEmpty(txtEmail.getText())) {
            JOptionPane.showMessageDialog(null, "Debe digitar un email", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

