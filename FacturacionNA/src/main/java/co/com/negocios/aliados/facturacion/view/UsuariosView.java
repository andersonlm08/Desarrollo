    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        logger.info("Inicio guardar usuario");
        UsuarioDTO usuarioDTO = new UsuarioDTO();
        usuarioDTO.setApellidos(txtApellidos.getText());
        usuarioDTO.setNombres(txtNombres.getText());
        usuarioDTO.setClave(new String(txtConfirmarClave.getPassword()));
        usuarioDTO.setDocumento(txtDocumento.getText());
        usuarioDTO.setEmail(txtEmail.getText());
        usuarioDTO.setTelefono(txtTelefono.getText());
        usuarioDTO.setUsuario(txtUsuario.getText());

        try {
            if (validarDatosObligatorios()) {
                if (bean.existeUsuario(txtUsuario.getText())) {
                    bean.guardarUsuario(usuarioDTO);
                    refrescarTabla();
                    limpiarCampos();
                    JOptionPane.showMessageDialog(null, "Registro exitoso", "MENSAJE DEL SISTEMA", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Ya se encuentra registrado el el usuario", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception ex) {
            logger.error(ExceptionUtils.getFullStackTrace(ex));
            JOptionPane.showMessageDialog(null, "Ocurrió un error inesperado, comuniquese con el adminstrador del sistema", "MENSAJE DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
        }
        logger.info("Fin guardar usuario");
    }//GEN-LAST:event_btnGuardarActionPerformed

